-- Tabla de Incidencias (Mantenimiento)
CREATE TABLE incidencias (
  id_incidencia NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tipo_falla VARCHAR2(50) NOT NULL,
  descripcion VARCHAR2(500) NOT NULL,
  foto VARCHAR2(255),
  fecha_reporte TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  fecha_resolucion TIMESTAMP,
  estado VARCHAR2(20) DEFAULT 'Pendiente' NOT NULL,
  id_habitacion NUMBER NOT NULL,
  id_usuario NUMBER NOT NULL,
  CONSTRAINT fk_incid_hab FOREIGN KEY (id_habitacion) REFERENCES habitaciones(id_habitacion),
  CONSTRAINT fk_incid_usu FOREIGN KEY (id_usuario) REFERENCES usuarios(id_usuario)
);

-- Tabla de Alertas Automáticas
CREATE TABLE alertas (
  id_alerta NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tipo_alerta VARCHAR2(50) NOT NULL,
  mensaje VARCHAR2(500) NOT NULL,
  fecha_creacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  leida CHAR(1) DEFAULT '0' NOT NULL,
  id_usuario NUMBER NOT NULL,
  CONSTRAINT fk_alerta_usu FOREIGN KEY (id_usuario) REFERENCES usuarios(id_usuario)
);

COMMIT;
